# =====================
# BASIC PRINT EXAMPLES
# =====================

# Basic print
print("Hello, world")

# Multiple values
print("Hello", "Richard")

# Numbers
print(10)
print(10 + 5)

# Variables
name = "Richard"
age = 25

print("My name is", name)
print("I am", age, "years old")

# f-strings (importante)
print(f"My name is {name} and I am {age} years old")

# Escape characters
print("Line1\nLine2")

# Separator
print("A", "B", "C", sep="-")

# End parameter
print("This is the end", end="!")
print(" Next line")

